    public void loadCategory() {
        List<Category> categories = controller.loadCategories();

        for (int i = 0; i < categories.size(); i++) {
            Category category = categories.get(i);
            event.addItem(category.name);
        }
    }

    private void reloadPackagesDataSource(int category_id) {
        packages = controller.loadPackages(category_id);

        paket.removeAllItems();
        for (int i = 0; i < packages.size(); i++) {
            Paket pkt = packages.get(i);
            paket.addItem(pkt.name);
        }

    }


    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        // TODO add your handling code here:

        try {

            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dhagrafis", "root", "calonmertua2002");
//        

            String sql = "insert into orders (nama,phone, detailalamat, waktu, paket_id, user_id, category_id, total, location_id)" + " values(?,?,?,?,?,?,?,?,?)";
//     

            PreparedStatement preparedStmt = conn.prepareStatement(sql);

            int selectPkt = paket.getSelectedIndex();
            int ctgr = event.getSelectedIndex();
            int loc = address.getSelectedIndex();

            //category
            preparedStmt.setString(1, name.getText());
            preparedStmt.setString(2, phone.getText());
            preparedStmt.setString(3, detAddress.getText());
            preparedStmt.setString(4, time.getText());
            preparedStmt.setInt(5, selectPkt);  //paket_id
            preparedStmt.setInt(6, 1);     //user_id
            preparedStmt.setInt(7, ctgr);     //category_id
            preparedStmt.setString(8, totPrice.getText());     //total
            preparedStmt.setInt(9, loc);        //location_id

            preparedStmt.execute();
            conn.close();

            JOptionPane.showMessageDialog(null, "Anda berhasil Booking !!\nsegera lakukan pembayaran");
            loadListBooking();
            loadListBook();
        } catch (SQLException ex) {
            Logger.getLogger(MainView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnOrderActionPerformed

